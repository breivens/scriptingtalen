def nurseryrhyme(animal='cow', here='moo', there='moo'):
    print(f'''Old MacDonald had a farm, E-I-E-I-O
and on his farm he had a {animal}, E-I-E-I-O
With a {here}-{there} here, and a {here}-{there} there
here a {here}, there a {there}
everywhere a {here}-{there}
Old MacDonald had a farm, E-I-E-I-O''')


nurseryrhyme()
